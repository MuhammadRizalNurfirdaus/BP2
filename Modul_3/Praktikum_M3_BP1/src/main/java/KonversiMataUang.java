    private void konversi() {
        try {
        double nilai = Double.parseDouble(jTextField1.getText());
        String pilihan = (String) jComboBox1.getSelectedItem();
        double hasil;

        switch (pilihan) {
            case "Rupiah ke Dollar":
                hasil = nilai / 15000;
                jTextArea1.setText(String.format("Hasil: $ %.2f", hasil));
                break;
            case "Rupiah ke Yuan":
                hasil = nilai / 2100;
                jTextArea1.setText(String.format("Hasil: Â¥ %.2f", hasil));
                break;
            case "Dollar ke Rupiah":
                hasil = nilai * 15000;
                jTextArea1.setText(String.format("Hasil: Rp %.2f", hasil));
                break;
            case "Yuan ke Rupiah":
                hasil = nilai * 2100;
                jTextArea1.setText(String.format("Hasil: Rp %.2f", hasil));
                break;
            default:
                jTextArea1.setText("Pilihan konversi tidak valid.");
        }

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
